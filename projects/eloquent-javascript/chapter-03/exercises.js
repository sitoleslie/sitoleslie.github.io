// find how many upperCase B's are in the string 
function countBs(str) {

// input: str 
// split it into an array
str = str.split('');

// use reduce 
// acc is 0 
// currV will be each element 
return str.reduce((acc, currV) => {
  // check if each element has upperCase B 
  if(currV === 'B'){
    // add to the acc
    acc++
  }
  // return 
  return acc;
}, 0);


}

 
