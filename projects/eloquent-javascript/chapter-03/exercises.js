// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////


function countChars(str, letter) {
 // input: str 
// split it into an array
str = str.split('');

// use reduce 
// acc is 0 
// currV will be each element 
return str.reduce((acc, currV) => {
  // check if each element has the letter 
  if(currV === letter){
    // add to the acc
    acc++
  }
  // return 
  return acc;
}, 0);


}

