function min(num1, num2) {
// i: two numbers 

// o: returns the minimum 

// containts: use math.min 

if(num1 > num2){
for(let i = num2; i <= num1; i--){
 return Math.min(i);
} 
} else if (num1 < num2){
  for(let i = num1; i <= num2; i++){
    return Math.min(i);
  }
} else {
  return Math.min(num1);
}


}

